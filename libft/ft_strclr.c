#include "libft.h"

void	ft_strclr(char *s)
{
	char	*tmp;
	int		i;

	if (s)
	{
		i = 0;
		tmp = s;
		while (tmp[i] != '\0')
		{
			tmp[i] = '\0';
			i++;
		}
	}
}
